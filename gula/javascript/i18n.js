// Initialize Translation Logic
let currentLang = localStorage.getItem("gula_lang") || "hu";

function applyTranslations(lang) {
    currentLang = lang;
    localStorage.setItem("gula_lang", lang);
    document.documentElement.lang = lang;

    // Find all translatable elements
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key] !== undefined) {
            const translation = translations[lang][key];
            
            // Check if element has HTML tags in translation, or is standard text
            if (translation.includes("<strong") || translation.includes("<br") || translation.includes("<i") || translation.includes("<span")) {
                el.innerHTML = translation;
            } else {
                el.textContent = translation;
            }
        }
    });

    // Translate page specific attributes if needed (e.g. data-content for menu links hover)
    const menuLinks = document.querySelectorAll(".menu-links");
    menuLinks.forEach(link => {
        const key = link.getAttribute("data-i18n-content");
        if (key && translations[lang] && translations[lang][key]) {
            link.setAttribute("data-content", translations[lang][key]);
        }
    });

    // Dynamic Title & Meta Description update for SEO
    let pageType = "index";
    if (window.location.pathname.includes("flottak")) pageType = "flottak";
    else if (window.location.pathname.includes("felvasarlas")) pageType = "felvasarlas";
    else if (window.location.pathname.includes("karrier")) pageType = "karrier";

    const titleKey = `meta.title.${pageType}`;
    const descKey = `meta.desc.${pageType}`;

    if (translations[lang] && translations[lang][titleKey]) {
        document.title = translations[lang][titleKey];
    }
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && translations[lang] && translations[lang][descKey]) {
        metaDesc.setAttribute("content", translations[lang][descKey]);
    }

    // Set active status on language selector button/dropdown
    const langSelect = document.getElementById("langSelect");
    if (langSelect) {
        langSelect.value = lang;
    }
    
    // Toggle active state classes on floating buttons if present
    document.querySelectorAll(".lang-switch-btn").forEach(btn => {
        if (btn.getAttribute("data-lang") === lang) {
            btn.classList.add("active");
        } else {
            btn.classList.remove("active");
        }
    });
}

// DomContentLoaded Listener
document.addEventListener("DOMContentLoaded", () => {
    // Initial translation load
    applyTranslations(currentLang);

    // Event listener for language select dropdown (if available in navbar)
    const langSelect = document.getElementById("langSelect");
    if (langSelect) {
        langSelect.addEventListener("change", (e) => {
            applyTranslations(e.target.value);
        });
    }

    // Event listener for dynamic language click buttons
    document.querySelectorAll(".lang-switch-btn").forEach(btn => {
        btn.addEventListener("click", (e) => {
            e.preventDefault();
            const selectedLang = btn.getAttribute("data-lang");
            applyTranslations(selectedLang);
        });
    });
});
